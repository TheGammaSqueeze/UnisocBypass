#!/usr/bin/env python3
"""Experiment 002: -25 mV UV across all GPU OPPs."""
import sys
from pathlib import Path

HERE = Path(__file__).parent
SRC_DTS = Path('/tmp/editor.dts')
OUT_DTS = HERE / 'modified.dts'

assert SRC_DTS.exists(), f"missing {SRC_DTS}"
dts = SRC_DTS.read_text()

ops_old = '<0x5dc00 0xaae60 0x7d000 0xb71b0 0x96000 0xb71b0 0xbb800 0xc3500 0xcf850 0xc3500>'
ops_new = '<0x5dc00 0xa4cb0 0x7d000 0xb1008 0x96000 0xb1008 0xbb800 0xbd358 0xcf850 0xbd358>'
assert dts.count(ops_old) == 1
dts = dts.replace(ops_old, ops_new)

lst_old = '<0x5dc00 0xaae60 0x04 0x01 0x7d000 0xb71b0 0x05 0x01 0x96000 0xb71b0 0x06 0x01 0xbb800 0xc3500 0x07 0x01 0xcf850 0xc3500 0x08 0x01>'
lst_new = '<0x5dc00 0xa4cb0 0x04 0x01 0x7d000 0xb1008 0x05 0x01 0x96000 0xb1008 0x06 0x01 0xbb800 0xbd358 0x07 0x01 0xcf850 0xbd358 0x08 0x01>'
assert dts.count(lst_old) == 1
dts = dts.replace(lst_old, lst_new)

OUT_DTS.write_text(dts)
print(f'wrote {OUT_DTS}')
